#include"Origin.h"
void PLL_init(void)
{
	SYSTCTL_RCC2_R |=0x80000000;
	SYSTCTL_RCC2_R |=0x00000800;
	
	SYSTCTL_RCC_R =(SYSTCTL_RCC_R & ~0x000007c0); //clear
	SYSTCTL_RCC_R |= 0x00000540;
	SYSTCTL_RCC2_R &= ~0x00000070;
	SYSTCTL_RCC2_R &=~0x00002000;
	SYSTCTL_RCC2_R |=0x40000000;
	SYSTCTL_RCC2_R =(SYSTCTL_RCC2_R &~0x1fc00000);
	SYSTCTL_RCC2_R |= (7<<22);
   while(	(SYSTCTL_RIS_R & 0x00000040)==0){};
	
	SYSTCTL_RCC2_R &=~0x00000800;
}


